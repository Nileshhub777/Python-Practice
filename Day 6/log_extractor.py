def log_extractor():
    new_ctr={}
    with open('abc.txt', 'r') as f:
        for line in f:
            level=line.split()[0] # INFO,WARN,ERROR

            if level in new_ctr:
                new_ctr[level]+=1
            else:
                new_ctr[level]=1
    return new_ctr

print(log_extractor())





















































































































































































